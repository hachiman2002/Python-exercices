"""
Write a program that allows you to enter 3 numbers and print the sum of
they.
"""
num_1 = float(input("Type the first number: "))
num_2 = float(input("Type the second number: "))
num_3 = float(input("Type the third number: "))
sum = num_1 + num_2 + num_3

print(f"Result sum : {sum:.1f} ")

